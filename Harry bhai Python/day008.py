"""
Operators
Python has different types of operators for different operations
"""
# Arithmetic operators
print(5+6); # Addition
print(15-6); # Subtraction
print(15*6); # Multiplication
print(15/6); # Division (it will give a decimal number)
print(15//6); # Floor Division
print(5%3); # Modulus
print(2**4); # Exponential
